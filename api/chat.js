function detectFigure(query) {
  const q = query.toLowerCase();
  // Sort by keyword length descending so longer/more specific matches win
  const sorted = Object.entries(FIGURE_KEYWORDS).sort(
    (a,b) => Math.max(...b[1].map(k=>k.length)) - Math.max(...a[1].map(k=>k.length))
  );
  for (const [figId, keywords] of sorted) {
    if (keywords.some(kw => q.includes(kw))) return figId;
  }
  return null;
}

